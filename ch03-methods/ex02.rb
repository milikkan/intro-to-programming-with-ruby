# What do the following expressions evaluate to?

x = 2          # evaluates to 2, assignment expressions evaluate to the assigned value
puts x = 2

puts x = 2     # evaluates to nil, puts return nil 

p name = "Joe" # evaluates to "Joe", p returns the value printed

four = "four"  # evaluates to "four"
puts four = "four"

print something = "nothing" # evaluates to nil because print returns nil